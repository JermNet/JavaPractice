public class LoopTest {

    public static void main(String[] args) throws InterruptedException {

        while (true) {
            System.out.println("\\");
            Thread.sleep(500);
            System.out.println(" \\");
            Thread.sleep(500);
            System.out.println("  \\");
            Thread.sleep(500);
            System.out.println("   \\");
            Thread.sleep(500);
            System.out.println("    \\");
            Thread.sleep(500);
            System.out.println("     \\");
            Thread.sleep(500);
            System.out.println("      \\");
            Thread.sleep(500);
            System.out.println("       \\");
            Thread.sleep(500);
            System.out.println("       /");
            Thread.sleep(500);
            System.out.println("      /");
            Thread.sleep(500);
            System.out.println("     /");
            Thread.sleep(500);
            System.out.println("    /");
            Thread.sleep(500);
            System.out.println("   /");
            Thread.sleep(500);
            System.out.println("  /");
            Thread.sleep(500);
            System.out.println(" /");
            Thread.sleep(500);
            System.out.println("/");
            Thread.sleep(500);
        }
    }

}
